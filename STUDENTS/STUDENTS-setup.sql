CREATE TABLE teachers (
    LastName VARCHAR(255) NOT NULL,
    FirstName VARCHAR(255) NOT NULL,
    Classroom INT NOT NULL
);

CREATE TABLE list (
    LastName VARCHAR(255) NOT NULL,
    FirstName VARCHAR(255) NOT NULL,
    Grade INT NOT NULL,
    Classroom INT NOT NULL
);